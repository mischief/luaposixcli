-- awk/eval_test.lua - busted tests for awk evaluator
package.cpath = "build/?.so;" .. package.cpath

local parser = require("awk.parser")
local eval = require("awk.eval")

local function run_awk(program, input_lines)
	local ast = parser.parse(program)
	local interp = eval.new()
	local output = {}
	interp.write = function(s) output[#output + 1] = s end
	input_lines = input_lines or {}
	local idx = 0
	local function get_record()
		idx = idx + 1
		return input_lines[idx]
	end
	interp:run(ast, get_record)
	return table.concat(output)
end

describe("awk eval", function()
	describe("field splitting", function()
		it("splits on whitespace by default", function()
			assert.equal("b\n", run_awk("{ print $2 }", { "a b c" }))
		end)
		it("splits on custom FS", function()
			assert.equal("2\n", run_awk('BEGIN{FS=":"} { print $2 }', { "1:2:3" }))
		end)
		it("handles NF", function()
			assert.equal("3\n", run_awk("{ print NF }", { "a b c" }))
		end)
		it("rebuilds $0 on field assignment", function()
			assert.equal("a X c\n", run_awk('{ $2="X"; print }', { "a b c" }))
		end)
		it("uses OFS when rebuilding", function()
			assert.equal("a,b,c\n", run_awk('BEGIN{OFS=","} { $1=$1; print }', { "a b c" }))
		end)
	end)

	describe("arithmetic", function()
		it("basic operators", function()
			assert.equal("15\n", run_awk("BEGIN{ print 3+4*3 }"))
		end)
		it("modulus", function()
			assert.equal("1\n", run_awk("BEGIN{ print 7%3 }"))
		end)
		it("exponentiation", function()
			assert.equal("8\n", run_awk("BEGIN{ print 2^3 }"))
		end)
		it("unary minus", function()
			assert.equal("-5\n", run_awk("BEGIN{ print -5 }"))
		end)
		it("increment/decrement", function()
			assert.equal("5 6\n", run_awk("BEGIN{ x=5; print x++, x }"))
		end)
		it("pre-increment", function()
			assert.equal("6 6\n", run_awk("BEGIN{ x=5; print ++x, x }"))
		end)
	end)

	describe("strings", function()
		it("concatenation", function()
			assert.equal("helloworld\n", run_awk('BEGIN{ print "hello" "world" }'))
		end)
		it("length", function()
			assert.equal("5\n", run_awk('BEGIN{ print length("hello") }'))
		end)
		it("substr", function()
			assert.equal("llo\n", run_awk('BEGIN{ print substr("hello", 3) }'))
		end)
		it("substr with length", function()
			assert.equal("el\n", run_awk('BEGIN{ print substr("hello", 2, 2) }'))
		end)
		it("index", function()
			assert.equal("3\n", run_awk('BEGIN{ print index("hello", "llo") }'))
		end)
		it("tolower/toupper", function()
			assert.equal("hello HELLO\n", run_awk('BEGIN{ print tolower("HELLO"), toupper("hello") }'))
		end)
		it("sprintf", function()
			assert.equal("[05]\n", run_awk('BEGIN{ print sprintf("[%02d]", 5) }'))
		end)
	end)

	describe("sub/gsub", function()
		it("sub replaces first match", function()
			assert.equal("hi world hello\n", run_awk('{ sub(/hello/, "hi"); print }', { "hello world hello" }))
		end)
		it("gsub replaces all", function()
			assert.equal("hi world hi\n", run_awk('{ gsub(/hello/, "hi"); print }', { "hello world hello" }))
		end)
		it("& in replacement", function()
			assert.equal("[hello] world\n", run_awk('{ sub(/hello/, "[&]"); print }', { "hello world" }))
		end)
	end)

	describe("arrays", function()
		it("basic set/get", function()
			assert.equal("bar\n", run_awk('BEGIN{ a["foo"]="bar"; print a["foo"] }'))
		end)
		it("in operator", function()
			assert.equal("1\n", run_awk('BEGIN{ a[1]=1; if (1 in a) print 1; else print 0 }'))
		end)
		it("delete", function()
			assert.equal("0\n", run_awk('BEGIN{ a[1]=1; delete a[1]; print (1 in a) }'))
		end)
		it("for-in", function()
			local out = run_awk('BEGIN{ a[1]="x"; a[2]="y"; for(k in a) n++; print n }')
			assert.equal("2\n", out)
		end)
		it("split", function()
			assert.equal("3 b\n", run_awk('BEGIN{ n=split("a:b:c", arr, ":"); print n, arr[2] }'))
		end)
	end)

	describe("control flow", function()
		it("if/else", function()
			assert.equal("yes\n", run_awk('BEGIN{ if (1) print "yes"; else print "no" }'))
		end)
		it("while", function()
			assert.equal("1\n2\n3\n", run_awk("BEGIN{ i=1; while(i<=3) { print i; i++ } }"))
		end)
		it("for", function()
			assert.equal("1\n2\n3\n", run_awk("BEGIN{ for(i=1;i<=3;i++) print i }"))
		end)
		it("do-while", function()
			assert.equal("1\n2\n3\n", run_awk("BEGIN{ i=1; do { print i; i++ } while(i<=3) }"))
		end)
		it("break", function()
			assert.equal("1\n2\n", run_awk("BEGIN{ for(i=1;i<=5;i++) { if(i==3) break; print i } }"))
		end)
		it("continue", function()
			assert.equal("1\n2\n4\n5\n", run_awk("BEGIN{ for(i=1;i<=5;i++) { if(i==3) continue; print i } }"))
		end)
		it("next", function()
			assert.equal("a\nc\n", run_awk('{ if($0=="b") next; print }', { "a", "b", "c" }))
		end)
	end)

	describe("patterns", function()
		it("regex pattern", function()
			assert.equal("bar\nbaz\n", run_awk("/ba/ { print }", { "foo", "bar", "baz" }))
		end)
		it("expression pattern", function()
			assert.equal("3\n", run_awk("NR==2 { print $1 }", { "1", "3", "5" }))
		end)
		it("range pattern", function()
			assert.equal("b\nc\nd\n", run_awk("/b/,/d/ { print }", { "a", "b", "c", "d", "e" }))
		end)
		it("BEGIN/END", function()
			assert.equal("start\n1\nend\n", run_awk('BEGIN{print "start"} {print NR} END{print "end"}', { "x" }))
		end)
	end)

	describe("user functions", function()
		it("basic function", function()
			assert.equal("7\n", run_awk("function add(a,b){return a+b} BEGIN{print add(3,4)}"))
		end)
		it("recursive function", function()
			assert.equal("120\n", run_awk("function fact(n){if(n<=1)return 1;return n*fact(n-1)} BEGIN{print fact(5)}"))
		end)
		it("local variables via extra params", function()
			assert.equal("1\n", run_awk("function f(a,   x){x=99;return a} BEGIN{print f(1)}"))
		end)
	end)

	describe("printf", function()
		it("format string", function()
			assert.equal("hello 042", run_awk('BEGIN{ printf "%s %03d", "hello", 42 }'))
		end)
		it("float format", function()
			assert.equal("3.14", run_awk('BEGIN{ printf "%.2f", 3.14159 }'))
		end)
	end)

	describe("getline", function()
		it("advances to next record", function()
			assert.equal("b\n", run_awk("NR==1 { getline; print }", { "a", "b", "c" }))
		end)
		it("getline into var", function()
			assert.equal("b\n", run_awk("NR==1 { getline x; print x }", { "a", "b", "c" }))
		end)
	end)

	describe("comparison", function()
		it("numeric comparison", function()
			assert.equal("1\n", run_awk('BEGIN{ print (10 > 9) }'))
		end)
		it("string comparison", function()
			assert.equal("1\n", run_awk('BEGIN{ print ("b" > "a") }'))
		end)
		it("regex match operator", function()
			assert.equal("1\n", run_awk('BEGIN{ if ("hello" ~ /ell/) print 1; else print 0 }'))
		end)
	end)

	describe("special variables", function()
		it("NR counts across files", function()
			assert.equal("3\n", run_awk("END{print NR}", { "a", "b", "c" }))
		end)
		it("ORS", function()
			assert.equal("a;b;", run_awk('BEGIN{ORS=";"} {print}', { "a", "b" }))
		end)
	end)
end)
